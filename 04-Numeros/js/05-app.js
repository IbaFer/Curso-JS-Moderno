
// Incrementos y decrementos


let puntaje= 5;

console.log(puntaje);


puntaje++; //manda llamar la variable y despues le agrega 1
++puntaje; //incrementa en 1 y se lo agrega a la variable


puntaje--;//manda llamar la variable y despues le quita 1
--puntaje;//decrementa en 1 y se lo agrega a la variable

//Si quieres incrementar o decrementar en una cantidad diferente a 1 

puntaje+=10; //el 10 representa la cantidad en la cual quieres incrementar

console.log(puntaje);