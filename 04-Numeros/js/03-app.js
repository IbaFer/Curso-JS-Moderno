//Objeto de math




let resultado = Math.PI;

console.log(resultado);



//redondear 

resultado= Math.round(2.9);
console.log(resultado);


//Redondear forzosamente hacia arriba
resultado= Math.ceil(2.1);
console.log(resultado);


//Redondear forzosamente hacia abajo
resultado= Math.floor(2.9);
console.log(resultado);



//Raiz cuadrada 
resultado = Math.sqrt(144);
console.log("La raiz cuadrada es: " + resultado);

//Valor absoluto
resultado= Math.abs(-588);
console.log("el valor absoluto es: " + resultado);


//Pontencia
resultado= Math.pow(8, 5);
console.log("La potencia es: " + resultado);


//Minimo
resultado = Math.min(3, 5, 12, 74)
console.log("El minimo es: " + resultado);

//Maximo
resultado = Math.max(3, 5, 12, 74)
console.log("El maximo es: " + resultado);

//Ramdom o aleatorio
resultado = Math.random() * 10; //El 10 nomas es para hacer el numero mas grande
console.log("Valor aleatorio: " + resultado);




